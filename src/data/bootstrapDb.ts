let logSeq = 1;
function log(
  partial: Omit<AuditLog, "id" | "createdAt"> & { createdAt?: string }
): AuditLog {
  return {
    ...partial,
    id: `log-${logSeq++}`,
    createdAt: partial.createdAt ?? new Date().toISOString(),
  };
}

